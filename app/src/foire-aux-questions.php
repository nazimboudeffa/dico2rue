<h1>FAQ ( Foire Aux Questions )</h1>


<img src="http://www.dico2rue.com/img/faq.jpg" alt="Dissection d'une définition"  />

<h1>1. Dico 2 Rue...c’est quoi ?</h1>
<p>Dico 2 Rue est un dictionnaire collaboratif spécialisé dans l’argot. Véritable site communautaire, ce sont les utilisateurs qui votent pour les meilleurs mots, créent les expressions, les définitions, les exemples et les synonymes. C’est le dico français d’argot par excellence.</p>
<br />
<br />
<h1>2. Comment ça marche ?</h1>
<p>Dico 2 Rue est un dictionnaire collaboratif. Ce sont les internautes qui votent pour leurs mots et expressions préférées. Les membres peuvent y créer des mots ou des expressions, leurs définitions, des exemples et des synonymes. Chaque membre y possède aussi un espace perso simple et efficace où il peut suivre ses statistiques. Les définitions les plus appréciées se retrouvent dans la section mot du jour.</p>
<br />
<br />
<h1>3. Comment évaluer un mot ?</h1>
<p>A côté de chaque mot ou expression vous trouverez un bouton yeah! : smiley vert (= évaluation positive)  et un bouton bof… : smiley rouge (=évaluation négative). Pour évaluer un mot vous cliquez sur un de ces deux boutons. Vous pouvez seulement cliquer une fois pour chaque mot ou </p>
<br />
<br />
<h1>4. Comment devenir membre ?</h1>
<p>Devenir membre prend seulement quelques secondes. Rendez-vous dans la section « S’enregistrer ». Un email de confirmation vous sera ensuite envoyé. Il faudra cliquer sur le lien d’activation pour terminer la création de votre compte.</p>
<br />
<br />
<h1>5. Comment se connecter à son compte ?</h1>
<p>Pour cela, cliquer sur « Se connecter » en haut à droite de votre page. Une fois connecté vous pourrez rajouter des mots, voter pour vos préférés et suivre vos statistiques dans la section  « Mon Compte »</p>
<br />
<br />
<h1>6. Comment soumettre un mot ou une expression ?</h1>
<p>Pour participer au dictionnaire en soumettant un mot ou expression :</p>
<p>1.  allez dans la section « Rajouter un mot ». Ensuite à vous de rajouter un mot ou expression, une définition, un exemple et des synonymes.</p>
Il y a quelques règles basiques à respecter.<br />
Essayez d’être drôles et instructifs à la fois (votre mot aura plus de chances d’être promu)<br />
Ecrivez pour un public large en essayant d’éviter les abréviations, le langage texto et les fautes d’orthographe <br />
N’écrivez pas de noms de personnes ou de blagues privées que seuls vous et vos amis comprendront<br />
<p>2.  Il vous sera demandé de créer un compte en deux secondes. Pour cela entrez votre pseudo, email et mot de passe. Si vous avez déjà un compte,  remplissez les champs identifiant et mot de passe.</p>
<p>3.  Un email vous sera envoyé avec un lien permettant de valider votre compte et votre mot. Si vous avez déjà un compte, aucun email vous sera envoyé, votre mot sera automatiquement validé.</p>
<br />
<br />
<h1>7. Qu’arrive-t-il à mon mot après la soumission ?</h1>
<p>Une fois validé, votre mot ou expression ira dans la section « Validez les nouveautés ». Ici les internautes pourront dire si votre soumission est assez bonne pour être intégrée dans le dictionnaire. Au bout de quelques votes positifs votre mot sera intégré et se retrouvera dans la section « Dictionnaire ». S’il reçoit trop de votes négatifs, il sera annulé et archivé.</p>
<br />
<br />
<h1>8. Comment retirer un mot ou expression que j’ai soumis ?</h1>
<p>Après la soumission d’un mot ou expression vous pouvez encore demander son retrait tant qu’il se trouve dans la section « Validez les nouveautés ».  Pour cela merci d’adresser un email avec votre identifiant et votre mot à contact @ dico2rue . com. Attention ! Une fois que votre mot est validé par les internautes et qu’il passe dans la section « Dictionnaire » il ne sera plus possible de le retirer.</p>
<br />
<br />
<h1>9. Comment un mot parvient-il dans la section « Mots du jour » ?</h1>
<p>Les mots de la section « Mots du jour » sont les mots les plus récents votés par les internautes.</p>
<br />
<br />
<h1>10. Comment signaler un mot ou expression qui vous semble déplacé ?</h1>
<p>Pour toute demande de retrait d’un mot ou d’une expression merci d’adresser un email à contact @ dico2rue . com<br />
Dico2rue  se garde le droit de toute décision finale quant à la modération ou non d’un mot.</p>
<br />
